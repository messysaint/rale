    // String parser
    public String[] tokenize( String s ) {
        
        StringTokenizer tokenString = new StringTokenizer(s);
        
        String[] returnValue = new String[tokenString.countTokens()];
        
        int i = 0;
        
        while( tokenString.hasMoreTokens()) {
            
            returnValue[i++] = tokenString.nextToken();
            
        }
        
        return returnValue;
        
    }
    
